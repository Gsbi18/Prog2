package main;

/**
 *
 * @author ancsangabor
 */
public class Pont {
    double x;
    double y;

    public Pont(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Pont{" + "x=" + x + ", y=" + y + '}';
    }
    
}
